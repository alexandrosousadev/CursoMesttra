//01: Faça um algoritmo utilizando a estrutura "do while" +
//        "" que imprima na tela os números de 1 à 10.
package EstruturaRepeticao;

public class Exercicio01 {
    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.println(i);
            i++;
        }while (i <= 10);
    }
}
